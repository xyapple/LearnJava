public class LoopThroughArray {
    public static void main(String[] args) {
        String[] hobbys = {"sports", "game", "movie"};
        System.out.println("Please loop though the hobbys: ");
        for(int i=0; i<= hobbys.length; i++){
            System.out.println(hobbys[i]);

        }
    }
}
