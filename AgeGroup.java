public class AgeGroup {
    public static void main(String[] args) {
        int age=25;
        if (age > 60){
            System.out.println("Elder");
        } else if (age > 40){
            System.out.println("Adult");
        } else if (age >18){
            System.out.println("Young Adult");
        } else{
            System.out.println("Youth");
        }

    }
}
